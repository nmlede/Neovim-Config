-- Basicos ----------------------------------------------------------------------------
vim.opt.backup = false                          -- Backup del archivo
vim.opt.clipboard = "unnamedplus"               -- Acceso al clipboard del sistema
vim.opt.fileencoding = "utf-8"                  -- Encoding
vim.opt.hlsearch = true                         -- Resaltar coincidencias de busqueda
vim.opt.ignorecase = true                       -- Ignorar mayusculas y minusculas al buscar
vim.opt.mouse = "a"                             -- Habilitar mouse
vim.opt.pumheight = 10                          -- Altura de pop up
vim.opt.showmode = false                        
vim.opt.showtabline = 2                         -- Siempre mostrar pestanias
vim.opt.smartcase = true                        
vim.opt.smartindent = true                      -- Indentado inteligente
vim.opt.splitbelow = true                       -- Todos los hsplit van abajo
vim.opt.splitright = true                       -- Todos los vsplit van a la derecha
vim.opt.termguicolors = true                    
vim.opt.timeoutlen = 1000                       -- Tiempo de espera para una secuencia mapeada
vim.opt.expandtab = true
vim.opt.shiftwidth = 2                          -- Espacios por indentacion
vim.opt.tabstop = 2                             -- Espacios por tabs
vim.opt.cursorline = true                       -- Resaltar lina actual
vim.opt.cursorcolumn = true                     -- Resaltar columna actual
vim.opt.number = true                           
vim.opt.numberwidth = 4                         -- Ancho de columnas
vim.opt.signcolumn = "yes"                      -- Mostrar siempre la altura del cursos
vim.opt.wrap = false                            -- No lineas largas
vim.opt.scrolloff = 8                           
vim.opt.sidescrolloff = 8
